# EXERCÍCIO 2: Calculadora Básica
# Objetivo: Aprender operadores matemáticos

# PASSO 1: Criar variáveis com números
a = 15
b = 2

# PASSO 2: Usar os operadores matemáticos
# + = soma
# - = subtração
# * = multiplicação
# / = divisão (retorna decimal)
# // = divisão inteira (retorna inteiro)
# % = módulo (retorna o resto da divisão)

print(f'A soma de {a} e {b} é: {a + b}')              # 17
print(f'A subtração de {a} e {b} é: {a - b}')         # 13
print(f'A multiplicação de {a} e {b} é: {a * b}')     # 30
print(f'A divisão de {a} e {b} é: {a / b}')           # 7.5
print(f'Divisão inteira: {a // b}')                     # 7
print(f'Resto da divisão: {a % b}')                     # 1

# COMO FUNCIONA:
# 15 + 2 = 17
# 15 - 2 = 13
# 15 * 2 = 30
# 15 / 2 = 7.5 (divisão normal retorna decimal)
# 15 // 2 = 7 (divisão inteira pega só a parte inteira)
# 15 % 2 = 1 (resto: 15 ÷ 2 = 7 com resto 1)
